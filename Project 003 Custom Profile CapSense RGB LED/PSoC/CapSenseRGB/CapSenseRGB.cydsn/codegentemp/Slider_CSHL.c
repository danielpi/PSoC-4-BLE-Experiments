    /*******************************************************************************
    * Function Name: Slider_AveragingFilter
    ********************************************************************************
    *
    * Summary:
    *  This is the Averaging filter function.
    *  The averaging filter looks at the three most recent samples of a position and
    *  reports the averaging value.
    *
    * Parameters:
    *  x1:  Current value.
    *  x2:  Previous value.
    *  x3:  Before previous value.
    *
    * Return:
    *  Returns filtered value.
	*
	* Global Variables:
	*  None.
	*
	* Side Effects:
	*  None
	* 
    *******************************************************************************/
    uint16 Slider_AveragingFilter(uint16 x1, uint16 x2, uint16 x3)
    {
        uint32 tmp = ((uint32)x1 + (uint32)x2 + (uint32)x3) / 3u;
        
        return ((uint16) tmp);
    }
#endif /* ( (0u != (Slider_RAW_FILTER_MASK & Slider_AVERAGING_FILTER) || \
