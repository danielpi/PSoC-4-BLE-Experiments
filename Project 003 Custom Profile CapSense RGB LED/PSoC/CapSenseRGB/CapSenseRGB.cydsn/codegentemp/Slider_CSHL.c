    /*******************************************************************************
    * Function Name: Slider_IIR2Filter
    ********************************************************************************
    *
    * Summary:
    *  This is the IIR1/2 filter function. IIR1/2 = 1/2current + 1/2previous.
    *
    * Parameters:
    *  x1:  Current value.
    *  x2:  Previous value.
    *
    * Return:
    *  Returns filtered value.
	*
	* Global Variables:
	*  None.
	*
	* Side Effects:
	*  None
	* 
    *******************************************************************************/
    uint16 Slider_IIR2Filter(uint16 x1, uint16 x2)
    {
        uint32 tmp;
        
        /* IIR = 1/2 Current Value+ 1/2 Previous Value */
        tmp = (uint32)x1 + (uint32)x2;
        tmp >>= 1u;
    
        return ((uint16) tmp);
    }
#endif /* ( (0u != (Slider_RAW_FILTER_MASK & Slider_IIR2_FILTER)) || \
       *    (0u != (Slider_POS_FILTERS_MASK & Slider_IIR2_FILTER)) )
