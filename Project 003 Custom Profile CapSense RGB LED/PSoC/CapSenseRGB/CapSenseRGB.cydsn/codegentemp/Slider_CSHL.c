/*******************************************************************************
* Function Name: Slider_UpdateEnabledBaselines
********************************************************************************
*
* Summary:
*  Checks Slider_sensorEnableMask[] array and calls the 
*  Slider_UpdateSensorBaseline function to update the baselines 
*  for the enabled sensors.
*
* Parameters:
*  None
*
* Return:
*  None
*
* Global Variables:
*  Slider_sensorEnableMask[] - used to store the sensor scanning 
*  state.
*  Slider_sensorEnableMask[0] contains the masked bits for sensors 
*   0 through 7 (sensor 0 is bit 0, sensor 1 is bit 1).
*  Slider_sensorEnableMask[1] contains the masked bits for 
*  sensors 8 through 15 (if needed), and so on.
*  0 - sensor doesn't scan by Slider_ScanEnabledWidgets().
*  1 - sensor scans by Slider_ScanEnabledWidgets().
* 
* Side Effects:
*  None
* 
*******************************************************************************/
 void Slider_UpdateEnabledBaselines(void)
{
    uint32 i;
    uint32 isSensorEnabled;
    
    for(i = 0u; i < Slider_TOTAL_SENSOR_COUNT; i++)
    {
        isSensorEnabled = Slider_GetBitValue(Slider_sensorEnableMask, i);
        
        if(0u != isSensorEnabled)
        {
            Slider_UpdateSensorBaseline(i);
        }
    }
}

/*******************************************************************************
* Function Name: Slider_UpdateWidgetBaseline
********************************************************************************
*
* Summary:
*  Checks Slider_sensorEnableMask[] array and calls the 
*  Slider_UpdateSensorBaseline function to update the baselines 
*  for the enabled sensors that belong to widget.
*
* Parameters:
*  widget:  Widget number.
*
* Return:
*  None
*
* Global Variables:
*  Slider_sensorEnableMask[] - used to store the sensor scanning 
*  state.
*  Slider_sensorEnableMask[0] contains the masked bits for sensors 
*   0 through 7 (sensor 0 is bit 0, sensor 1 is bit 1).
*  Slider_sensorEnableMask[1] contains the masked bits for 
*  sensors 8 through 15 (if needed), and so on.
*  0 - sensor doesn't scan by Slider_ScanEnabledWidgets().
*  1 - sensor scans by Slider_ScanEnabledWidgets().
* 
* Side Effects:
*  None
* 
*******************************************************************************/
void Slider_UpdateWidgetBaseline(uint32 widget)
{
    uint32 sensorsPerWidget;
    uint32 firstSensor;
    uint32 lastSensor;
    uint32 i;
    uint32 isSensorEnabled;
    
	/* Get number of sensors in widget */
	sensorsPerWidget = Slider_numberOfSensors[widget];

	/* Get first sensor in widget */
	firstSensor = Slider_rawDataIndex[widget];
	
	/* Get last sensor in widget */
	lastSensor = (Slider_rawDataIndex[widget] + sensorsPerWidget);
	
    for(i = firstSensor; i < lastSensor; i++)
    {
        isSensorEnabled = Slider_GetBitValue(Slider_sensorEnableMask, i);
        
        if(0u != isSensorEnabled)
        {
            Slider_UpdateSensorBaseline(i);
        }
    }
}


