{

	    uint32 i;
    uint32 state = 0u;
    
    for(i = 0u; i < Slider_TOTAL_WIDGET_COUNT; i++)
    {
        if (Slider_CheckIsWidgetActive(i) != 0u)
        {
            state = Slider_WIDGET_IS_ACTIVE;
        }
    }
    


    return state;
}


